console.log("Hello, world"); // used for general-purpose logging
console.info("Info"); // used to log informational messages explicitly
console.error("This is an error!"); // specifically for logging error messages
console.warn("This is a warning."); // used for logging warning messages
console.time("Timer"); // starts a timer that can be used to compute the duration of an operation
console.timeEnd("Timer"); // stops a timer that was previously started by calling console.time()
